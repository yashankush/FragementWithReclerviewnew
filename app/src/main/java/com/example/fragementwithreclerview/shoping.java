package com.example.fragementwithreclerview;

public class shoping {
    private int photo;
    private String name;
    private String phone;

    public shoping() {



    }

    public shoping(int photo, String name, String phone) {
        this.photo = photo;
        this.name = name;
        this.phone = phone;
    }

    public int getPhoto() {
        return photo;
    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }


    public void setPhoto(int photo) {
        this.photo = photo;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}




